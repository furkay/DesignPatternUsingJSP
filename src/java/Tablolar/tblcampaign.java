package Tablolar;

public class tblcampaign {

    private int pkCampaignID;
    private String MenuName;
    private String Meal;
    private String Drink;
    private String Dessert;
    private String MenuCurrency;

    public int getpkCampaignID() {
        return pkCampaignID;
    }
    public void setpkCampaignID(int pkCampaignID) {
        this.pkCampaignID = pkCampaignID;
    }
    public String getMenuName() {
        return MenuName;
    }
    public void setMenuName(String MenuName) {
        this.MenuName = MenuName;
    }
    public String getMeal() {
        return Meal;
    }
    public void setMeal(String Meal) {
        this.Meal = Meal;
    }
    public String getDrink() {
        return Drink;
    }
    public void setDrink(String Drink) {
        this.Drink = Drink;
    }
    public String getDessert() {
        return Dessert;
    }
    public void setDessert(String Dessert) {
        this.Dessert = Dessert;
    }
    public String getMenuCurrency() {
        return MenuCurrency;
    }
    public void setMenuCurrency(String MenuCurrency) {
        this.MenuCurrency = MenuCurrency;
    }
}



